from termcolor import colored
import time
import os

def load():
    os.system('cls')
    print(colored('[     ]', 'yellow'))
    time.sleep(1)
    os.system('cls')
    print(colored('[█    ]', 'yellow'))
    time.sleep(1)
    os.system('cls')
    print(colored('[██   ]', 'yellow'))
    time.sleep(1)
    os.system('cls')
    print(colored('[███  ]', 'yellow'))
    time.sleep(1)
    os.system('cls')
    print(colored('[████ ]', 'yellow'))
    time.sleep(1)
    os.system('cls')
    print(colored('[█████]', 'yellow'))
    time.sleep(1)
    os.system('cls')
    print(colored('Done!', 'green'))

load()
